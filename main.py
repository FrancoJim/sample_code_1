    coins = (("Silver Dollar", 1), ("Half Dollar", 0.5), ("Quarter", 0.25), ("Dime", 0.1), ("Nickel", 0.05), ("Penny", 0.01) )

    def __init__(self, total):
        self.total = total

    def __enter__(self):
        return self

    def __repr__(self):
        return f'ChaChing ({self.total})'

    def __str__(self):
        return f'ChaChing with ${self.total}'

    def __bytes__(self):
        return self.total.to_bytes(4, byteorder='big')

    def __exit__(self, exc_type, exc_value, traceback):
        return exc_type, exc_value, traceback

    def minimum_coins(self):
        change = {}
        total = self.total * 100
        for coin, denom in self.coins:
            moniez = 0
            denom *= 100
            while total >= denom:
                moniez += 1
                total -= denom
            change[coin] = moniez
        return f"${self.total:.2f} => {change}"


